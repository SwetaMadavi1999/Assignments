public class Main {
	public static void main(String[] args) {
		int a=10;
		int b=20;
		int temp;
System.out.println("before swapping a:"+a+" and b:"+b);
temp=a;
a=b;
b=temp;
System.out.println("after swapping a:"+a+ " and b :"+b);
	}
}
/* Output:
before swapping a:10 and b:20
after swapping a:20 and b:10
*/
